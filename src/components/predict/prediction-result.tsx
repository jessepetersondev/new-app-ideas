"use client";

import { useState } from "react";
import { Copy, RotateCcw, AlertCircle, CreditCard, RefreshCw } from "lucide-react";
import { toast } from "sonner";
import { Button } from "@/components/ui/button";
import { Card } from "@/components/ui/card";
import { Skeleton } from "@/components/ui/skeleton";
import { ViralityPrediction } from "@/types/prediction";
import { FactorBreakdown } from "./factor-breakdown";
import { SuggestionsList } from "./suggestions-list";
import { ViralScoreGauge } from "./viral-score-gauge";

interface PredictionResultProps {
  prediction: ViralityPrediction | null;
  isLoading: boolean;
  error: string | null;
  onReset: () => void;
  onRetry?: () => void;
}

export function PredictionResult({
  prediction,
  isLoading,
  error,
  onReset,
  onRetry,
}: PredictionResultProps) {
  const [copied, setCopied] = useState(false);

  // Handle copy results
  const handleCopyResults = async () => {
    if (!prediction) return;

    const formattedText = `
ðŸŽ¯ Virality Score: ${prediction.viralityScore}/100
ðŸ“Š Confidence: ${Math.round(prediction.confidence * 100)}%

ðŸ“ˆ Factor Breakdown:
â€¢ Sentiment: ${prediction.factors.sentiment.label} (${Math.round(prediction.factors.sentiment.score * 100)}%)
â€¢ Hashtags: ${prediction.factors.hashtags.count} tags${prediction.factors.hashtags.trending ? " (trending)" : ""}
â€¢ Length: ${prediction.factors.length.characters} characters${prediction.factors.length.optimal ? " (optimal)" : ""}
â€¢ Emojis: ${prediction.factors.emojis.count} emojis (${prediction.factors.emojis.impact} impact)
â€¢ Buzzwords: ${prediction.factors.buzzwords.count} buzzwords

ðŸ’¡ Suggestions:
${prediction.suggestions.map((s, i) => `${i + 1}. ${s}`).join("\n")}

Generated by Twitter/X Post Virality Predictor
    `.trim();

    try {
      await navigator.clipboard.writeText(formattedText);
      setCopied(true);
      toast.success("Results copied to clipboard!");
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      console.error("Failed to copy:", err);
      toast.error("Failed to copy results. Please try again.");
    }
  };

  // Loading state
  if (isLoading) {
    return (
      <div className="space-y-4 sm:space-y-6">
        <Card className="p-6 sm:p-8">
          <div className="flex flex-col items-center gap-4">
            <Skeleton className="h-[160px] w-[160px] sm:h-[200px] sm:w-[200px] rounded-full" />
            <div className="space-y-2 text-center">
              <Skeleton className="h-6 sm:h-8 w-24 sm:w-32 mx-auto" />
              <Skeleton className="h-3 sm:h-4 w-32 sm:w-40 mx-auto" />
            </div>
          </div>
        </Card>

        <Card className="p-4 sm:p-6">
          <Skeleton className="h-5 sm:h-6 w-32 sm:w-40 mb-3 sm:mb-4" />
          <div className="space-y-3 sm:space-y-4">
            {[1, 2, 3, 4, 5].map((i) => (
              <div key={i} className="space-y-2">
                <div className="flex items-center justify-between">
                  <Skeleton className="h-8 sm:h-10 w-24 sm:w-32" />
                  <Skeleton className="h-5 sm:h-6 w-10 sm:w-12" />
                </div>
                <Skeleton className="h-2 w-full" />
              </div>
            ))}
          </div>
        </Card>

        <Card className="p-4 sm:p-6">
          <Skeleton className="h-5 sm:h-6 w-48 sm:w-60 mb-3 sm:mb-4" />
          <div className="space-y-3">
            {[1, 2, 3].map((i) => (
              <Skeleton key={i} className="h-5 sm:h-6 w-full" />
            ))}
          </div>
        </Card>
      </div>
    );
  }

  // Error state
  if (error) {
    // Check if it's a rate limit error
    const isRateLimitError = error.toLowerCase().includes("limit") || error.toLowerCase().includes("daily");
    const isNetworkError = error.toLowerCase().includes("network") || error.toLowerCase().includes("connection");

    return (
      <Card className="p-6 sm:p-8">
        <div className="flex flex-col items-center gap-4 text-center">
          <div className="p-3 bg-destructive/10 rounded-full">
            <AlertCircle className="h-7 w-7 sm:h-8 sm:w-8 text-destructive" />
          </div>
          <div className="space-y-2">
            <h3 className="text-base sm:text-lg font-semibold">
              {isRateLimitError ? "Daily Limit Reached" : "Prediction Failed"}
            </h3>
            <p className="text-sm text-muted-foreground max-w-md px-2">
              {error}
            </p>
          </div>

          <div className="flex flex-col sm:flex-row gap-2 sm:gap-3 mt-2 w-full sm:w-auto">
            {isNetworkError && onRetry && (
              <Button onClick={onRetry} variant="default" className="min-h-[44px] w-full sm:w-auto">
                <RefreshCw className="h-4 w-4 mr-2" />
                Retry
              </Button>
            )}
            {isRateLimitError ? (
              <>
                <Button onClick={onReset} variant="outline" className="min-h-[44px] w-full sm:w-auto">
                  <RotateCcw className="h-4 w-4 mr-2" />
                  Go Back
                </Button>
                <Button variant="default" disabled className="min-h-[44px] w-full sm:w-auto">
                  <CreditCard className="h-4 w-4 mr-2" />
                  Upgrade (Coming Soon)
                </Button>
              </>
            ) : (
              <Button onClick={onReset} variant="outline" className="min-h-[44px] w-full sm:w-auto">
                <RotateCcw className="h-4 w-4 mr-2" />
                Try Again
              </Button>
            )}
          </div>

          {isRateLimitError && (
            <div className="mt-4 p-3 sm:p-4 bg-muted rounded-md max-w-md">
              <p className="text-xs text-muted-foreground">
                Your daily predictions reset at midnight UTC. Upgrade to get unlimited predictions, advanced analytics, and more!
              </p>
            </div>
          )}
        </div>
      </Card>
    );
  }

  // Empty state
  if (!prediction) {
    return (
      <Card className="p-6 sm:p-8">
        <div className="flex flex-col items-center gap-4 text-center">
          <div className="text-5xl sm:text-6xl" aria-hidden="true">
            ðŸ“Š
          </div>
          <div className="space-y-2">
            <h3 className="text-base sm:text-lg font-semibold">No Prediction Yet</h3>
            <p className="text-sm text-muted-foreground max-w-md px-2">
              Enter your post text above and click "Analyze" to see
              your results here.
            </p>
          </div>
        </div>
      </Card>
    );
  }

  // Success state with results
  return (
    <div className="space-y-4 sm:space-y-6">
      {/* Action buttons */}
      <div className="flex flex-col sm:flex-row items-stretch sm:items-center justify-end gap-2">
        <Button
          onClick={handleCopyResults}
          variant="outline"
          size="sm"
          disabled={copied}
          className="min-h-[44px] w-full sm:w-auto"
        >
          <Copy className="h-4 w-4 mr-2" />
          {copied ? "Copied!" : "Copy Results"}
        </Button>
        <Button onClick={onReset} variant="outline" size="sm" className="min-h-[44px] w-full sm:w-auto">
          <RotateCcw className="h-4 w-4 mr-2" />
          Try Another
        </Button>
      </div>

      {/* Viral Score Gauge */}
      <Card className="p-6 sm:p-8">
        <ViralScoreGauge score={prediction.viralityScore} />
        <div className="mt-3 sm:mt-4 text-center">
          <p className="text-xs sm:text-sm text-muted-foreground">
            Confidence: {Math.round(prediction.confidence * 100)}%
          </p>
        </div>
      </Card>

      {/* Factor Breakdown */}
      <FactorBreakdown prediction={prediction} />

      {/* Suggestions List */}
      <SuggestionsList suggestions={prediction.suggestions} />
    </div>
  );
}
